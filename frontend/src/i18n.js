// We create a reverse map to go from Marathi to English easily
const reverseTranslations = {};
for (const [en, mr] of Object.entries(translations)) {
    reverseTranslations[mr] = en;
}

let currentLang = localStorage.getItem('appLang') || 'en';

function applyTranslations(node) {
    if (node.nodeType === Node.TEXT_NODE) {
        let text = node.textContent.trim();
        if (!text) return;
        
        let map = currentLang === 'mr' ? translations : reverseTranslations;
        
        // Try to replace substrings if exact match isn't found (for combined texts)
        let newText = node.textContent;
        let changed = false;
        // Sort keys by length descending to replace longest phrases first
        const keys = Object.keys(map).sort((a,b) => b.length - a.length);
        for(let key of keys) {
            if (newText.includes(key)) {
                newText = newText.split(key).join(map[key]);
                changed = true;
            }
        }
        if (changed) {
            node.textContent = newText;
        }
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;
        if (node.placeholder) {
            let map = currentLang === 'mr' ? translations : reverseTranslations;
            let keys = Object.keys(map).sort((a,b) => b.length - a.length);
            for(let key of keys) {
                if (node.placeholder.includes(key)) {
                    node.placeholder = node.placeholder.split(key).join(map[key]);
                }
            }
        }
        for (let child of node.childNodes) {
            applyTranslations(child);
        }
    }
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'mr' : 'en';
    localStorage.setItem('appLang', currentLang);
    applyTranslations(document.body);
    
    // Update the toggle button text explicitly
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
        btn.textContent = currentLang === 'en' ? 'मराठी' : 'English';
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Initial translation if required
    if (currentLang === 'mr') {
        applyTranslations(document.body);
    }
    
    document.querySelectorAll('.lang-toggle-btn').forEach(btn => {
        btn.textContent = currentLang === 'en' ? 'मराठी' : 'English';
        btn.addEventListener('click', toggleLanguage);
    });
});
